/*
 * 1. Repository (Repo):
 * A repository is a storage space for your project. It can be local (on your computer)
 * or remote (hosted on platforms like GitHub or GitLab). A repo contains all your project
 * files, including the history of all changes made to those files.
 */

/*
 * 2. Commit:
 * A commit is a snapshot of your changes at a specific point in time. Each commit is
 * identified by a unique SHA-1 hash. Commits allow you to track the history of your project
 * and revert to previous states if needed. A commit typically includes a message describing
 * what changes were made.
 */

/*
 * 3. Branch:
 * A branch is a parallel version of your repository. By creating branches, you can work
 * on new features or bug fixes independently from the main codebase (often referred to as
 * the 'main' or 'master' branch). This helps isolate changes until they are ready to be merged.
 */

/*
 * 4. Merge:
 * Merging is the process of combining changes from different branches into one. When you
 * merge a branch into another, Git takes the commits from one branch and integrates them
 * into the other. If there are conflicting changes, you will need to resolve these conflicts
 * manually.
 */

/*
 * 5. Pull Request (PR):
 * A pull request is a request to merge changes from one branch into another, often used
 * in collaborative environments. It allows team members to review the proposed changes,
 * discuss them, and provide feedback before the changes are merged.
 */

/*
 * 6. Clone:
 * Cloning is the process of creating a copy of a remote repository on your local machine.
 * This allows you to work on the project offline and make changes locally before pushing
 * them back to the remote repo.
 */

/*
 * 7. Fork:
 * Forking is creating a personal copy of someone else's repository on your own account.
 * This allows you to experiment with changes without affecting the original project.
 * You can propose changes back to the original repository through pull requests.
 */

/*
 * 8. Staging Area:
 * The staging area is an intermediate space where changes are prepared before committing.
 * You use the command `git add` to move changes to the staging area. Only changes in
 * the staging area are included in the next commit.
 */

/*
 * 9. Remote:
 * A remote is a version of your repository that is hosted on the internet. Common commands
 * associated with remotes are `git push` (to send your local commits to the remote)
 * and `git pull` (to fetch and merge changes from the remote to your local repo).
 */

/*
 * 10. HEAD:
 * HEAD is a pointer that represents the current branch or commit you are working on.
 * It allows Git to know which commit is the latest in the context of your working directory.
 */

/*
 * 11. Rebase:
 * Rebasing is the process of moving or combining a series of commits to a new base commit.
 * It allows for a cleaner project history by avoiding unnecessary merge commits and can
 * help keep the commit history linear.
 */

/*
 * 12. Conflict:
 * A conflict occurs when changes from different branches cannot be automatically merged
 * due to conflicting modifications in the same part of a file. You will need to resolve
 * these conflicts manually before completing the merge.
 */

/*
 * 13. Tag:
 * A tag is a reference to a specific commit in the repository, often used to mark release
 * points. Tags are typically immutable and serve as bookmarks in the project's history.
 */

/*
 * 14. Cherry-Pick:
 * Cherry-picking is the process of selecting specific commits from one branch and applying
 * them to another branch. This is useful for applying only certain changes without merging
 * an entire branch.
 */

/*
 * 15. Git Flow:
 * Git Flow is a branching model that provides a structured way to manage features,
 * releases, and hotfixes in your project. It defines specific branches for different purposes,
 * helping teams maintain a clean and organized workflow.
 */
