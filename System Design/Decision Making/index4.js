// why use s3 over realtional db ?

// S3 is highly scalable, handling massive data volumes automatically.
// S3 is cost-efficient with a pay-as-you-go pricing model, ideal for large data.
// S3 offers high durability and availability with data replicated across multiple regions.
// S3 is optimized for high-throughput, low-latency access to large objects.
// S3 integrates seamlessly with AWS services for complex workflows.
// S3 provides robust security, including encryption and access controls.
// S3 is a managed service, reducing operational overhead like scaling and backups.
// S3 supports versioning and lifecycle policies for efficient data management.
